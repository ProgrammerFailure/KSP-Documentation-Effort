	public static Color ParseColor(string col)
	{
		if (m_colors.ContainsKey(col))
		{
			return m_colors[col];
		}
		return ParseColorHex(col);
	}

	public static Color ParseColorHex(string col)
	{
		Color result = Color.black;
		int length = col.Length;
		if (length > 0 && col[0] == "#"[0])
		{
			switch (length)
			{
			case 7:
			case 9:
			{
				float num = 0.003921569f;
				result.r = (float)((HexToDecimal(col[1]) << 4) | HexToDecimal(col[2])) * num;
				result.g = (float)((HexToDecimal(col[3]) << 4) | HexToDecimal(col[4])) * num;
				result.b = (float)((HexToDecimal(col[5]) << 4) | HexToDecimal(col[6])) * num;
				if (length == 9)
				{
					result.a = (float)((HexToDecimal(col[7]) << 4) | HexToDecimal(col[8])) * num;
				}
				break;
			}
			case 4:
			case 5:
			{
				float num = 1f / 15f;
				result.r = (float)HexToDecimal(col[1]) * num;
				result.g = (float)HexToDecimal(col[2]) * num;
				result.b = (float)HexToDecimal(col[3]) * num;
				if (length == 5)
				{
					result.a = (float)HexToDecimal(col[4]) * num;
				}
				break;
			}
			}
		}
		return result;
	}

	public static int HexToDecimal(char ch)
	{
		switch (ch)
		{
		case '0':
			return 0;
		case '1':
			return 1;
		case '2':
			return 2;
		case '3':
			return 3;
		case '4':
			return 4;
		case '5':
			return 5;
		case '6':
			return 6;
		case '7':
			return 7;
		case '8':
			return 8;
		case '9':
			return 9;
		default:
			return 0;
		case 'A':
		case 'a':
			return 10;
		case 'B':
		case 'b':
			return 11;
		case 'C':
		case 'c':
			return 12;
		case 'D':
		case 'd':
			return 13;
		case 'E':
		case 'e':
			return 14;
		case 'F':
		case 'f':
			return 15;
		}
	}
